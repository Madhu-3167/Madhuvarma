package com.BasicJava;

public class DoWhileExample {

	public static void main(String[] args) {
		
		int p=21;
		do
		{
			System.out.println("LoadRunner");
			p=p+1;
			
		}while(p<24);
		
		System.out.println("*******************");
		
		
		int k=21;
		
		do
		{
			System.out.println(k+" - "+"Testing");
			k=k-1;
		}while(k<=19);
		
		

	}

}
