package com.BasicJava;

public class Employee_VariableExample {

	public static void main(String[] args) {
		
		String employee_Name="Ram";
		int employee_Id=101;
		int employee_Age=21;
		char employee_Gender='M';
		double employee_Salary=18000.258;
		
		System.out.println(" The Name of the Employee is :"+employee_Name);
		System.out.println(" The Employee id is :"+employee_Id);
		System.out.println(" The Employee Gender is :"+employee_Gender);
		System.out.println(" The Employee Age is :"+employee_Age);
		System.out.println(" The Employee Salary is :"+employee_Salary);
		

	}

}
